                            <h3 style="color:black">6.Binsar Wildlife Sanctuary </h3>
                            <p>Set deep in the foothills of the Great Indian Himalayas, Binsar is a  small protected area, that was designated as a wildlife sanctuary in 1988, following a long local movement for its establishment. Today, Binsar supports a wide variety of floral species, faunal species as well as avi-fauna including some of the unique species found in the Himalayan range. Within Binsar's boundary, there a nine villages populated by around 600 people.
                            Binsar Wildlife Sanctuary has only one entry gate for tourists at Ayarpani and only one road, climbing in a north-west direction to terminate at the Forest Rest House. The sanctuary is criss-crossed by many tails, some relatively wide and well-trodden while some barely discernible. A walk through this sanctuary is a truly fascinating as well as refreshing as most of the park area is shaded with a thick forests</p>

                            <p>Within the Binsar Wildlife Sanctuary, 25 types of trees, 24 types of bushes and seven varieties of grasses are found. At the higher altitude, Binsar is dominated by the Oak and Rhododendron forests, while at the lower level, mostly you will find the Chir Pine forests.
                            According to recent census, Binsar Wildlife Sanctuary is home to 16 Leopards, 69 Ghorals, 57 Wild Boars, 43 Kakars, 150 Monkeys and two Himalayan Black Bears. In addition to these, the sanctuary provides shelter to other wild animals like Indian Red Fox, Jackals, Pine Martens and Porcupines. Apart from these, Binsar Wildlife Sanctuary ranks high in avian species variety and visibility. Here, you can see around 200 species of birds including both resident and migratory species.</p>
                                            
                                        

                            <h3 style="color:black">3. Assan Barrage Bird Sanctuary </h3>   

                            <p>The wetlands at Assan Barrage is located near the junction of two rivers : Yamuna &amp; Assan. The area hosts a large number of birds and waterfowls, many of which are listed as globally endangered by the IUCN Red Data Book. </p>

                            <p>Asan Barrage serves as a winter migration halt for trans-Himalayan migratory birds. These birds are spotted here on their way to southern India (in October-November) and again on their way back (in late February-March). By the end of October the Asan witnesses the arrival of migrants from Pale arctic region. <strong>The first to arrive is the shoveller followed by ruddy shelduck, mallard, coot, cormorant, egrets, wagtails, pond heron to name a few. Birds of prey like pallas fishing eagle, marsh harrier, greater spotted eagle, osprey and steppe eagle add to this magnificent diversity.</strong></p>
                            <p>The Assan Barrage Bird Sanctuary attracts around 53 species of avian fauna out of which 19 are winter migratory birds from Eurasia. The population of birds consists of Pintail , Red Crested Pochard, Ruddy Shelduck , Common Pochard, Mallard , Coot , Eurasian Wigeon , Gadwall , Common Teal , Shoveller , Egrets , Herons , Tufted Duck  and Lapwing .</p>
                               

